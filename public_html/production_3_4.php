Обеспериватель птицы линейный балочный тип SLВ служит для выдергивания пера разных пород птицы (бройлер, утка, и т.п.).
<BR><IMG src="images/point.gif" border="0" width="1" height="30" alt=""><BR>
<B>Строение и принцип действия:</B>
<P>
Несущая конструкция с корпусом изготовлены из нержавеющей стали, кислотостойкой. Головки на одной балке приводятся в действие отдельным электродвигателем в корпусе с помощью специальных шестерен. Обеспериватель оборудован ручными механическими подъемниками, что позволяет регулировать балки с головками по высоте. В зависимости от величины птицы, которая поступает на общипывание, можно должным образом выставить балки с головками. 
<BR><BR>
<p><B>Технические данные:</B></p>
<P>
Используемая мощность: 6 x 2,2 kW
<BR>Напряжение: 380 V
<BR>Количество балок: 6 шт.
<BR>Количество головок: 72 шт.
<BR>Количество резиновых пальцев в одной головке: 12шт.
