<B>Оборудование и действие:</B>
<P>
Ванна обескровливания изготовлена из нержавеющих кислотостойких материалов. Тушки птицы транспортируются по подвесному конвейеру над ванной обескровливания, где происходит стекание крови в открытую емкость. По сторонам ванны монтируются стенки, которые предупреждают разбрызгивание крови в помещении цеха. Длина, ширина (B), высота (H) в конструкции желоба зависит от времени обескровливания и производительности и трассы линии забоя птицы.
