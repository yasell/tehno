<B>Строение и принцип действия:</B>
<P>
Основным элементом стандартного подвесного конвейера является несущая цепь с шагом 1", хотя за специальным заказом клиента можно установить цепь с другим шагом. В состав подвесного конвейера входят: привод, натяжное устройство, повороты 90о и 180о, прямые сегменты, вертикальные повороты, цепные тележки, стремена, соединения. Цепной конвейер подвешен к несущей конструкции, выполненной отдельно согласно условий. В зависимости от длины и нагрузки конвейер может быть оснащен одним или несколькими  приводами и натяжными устройствами.
<p><B>Преимущество:</B></p>
<P>
- строение отдельных элементов, а также принципы их соединения обеспечивают разную форму и ход конвейера, в соответствии с потребностями клиента;
<BR>- простая конструкция позволяет быстро и легко заменить элементы или расширить возможности всего конвейера;
<BR>- оборудование конвейера с основным элементом - несущей цепью 1" позволяет применение разных размеров стремян - на расстоянии, что отвечает размеру птицы, которая транспортируется;
<BR>- простая и "прозрачная" конструкция обеспечивает, содержание конвейера  в необходимой чистоте и надлежащем обслуживании;
<BR>- применение высококачественных материалов обеспечивает прочность и стойкую работу оборудования.
