				<h1 class="title  title--red"> Насос для транспортировки пера Тип PZ </h1>
				Насос для пера служит для транспортировки пера полученного в процессе ощипывания птицы. Перо должно транспортироваться вместе с водой. 
<BR><IMG src="images/point.gif" border="0" width="1" height="30" alt=""><BR>
<B>Строение и принцип действия:</B>
<P>
Основным элементом является одноступенчатый насос Тип  PZ  в комплекте с двигателем.
Односторонняя крыльчатка с свободным протоком имеет лопасти, расположенные на диске. Диск крыльчатки смещен с проточного пространства и  размещен в полости насоса, что увеличивает проток между лопастями крыльчатки и вставкой корпуса.
Насос пера может устанавливаться как в существующих бетонных и стальных резервуарах так и  вместе с свободностоящим резервуаром, который поставляется вместе с насосом. Обычно, перо,  при помощи воды транспортируется в резервуар по сточным каналам. Позже, перо перекачивается насосом по трубопроводу диаметром 125-250мм ( в зависимости от вида насоса) на сепаратор пера, где происходит отделение воды.
