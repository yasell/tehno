				<h1 class="title  title--red"> Сбрасыватель тушек Тип WT - 1 </h1>
				Сбрасыватель тушек служит для сброса туш птицы из стремян подвесного конвейера на линии потрошения.

<p><B>Строение и принцип действия:</B></p>
<P>
Сбрасыватель тушек птицы изготовлен из нержавеющего, кислотостойкого материала, в подвесной версии к несущей конструкции конвейера. Тушки птицы, которые находятся на стременах, транспортируются подвесным цепным конвейером к направляющей сбрасывателя, где происходит сброс тушек из стремян конвейера. Привод на сбрасыватель передается от цепного конвейера через подобранное колесо.
