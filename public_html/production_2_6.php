				Помпа для крови служит для транспортировки крови из ванны обескровливания к пункту назначения.
<BR>
<p><B>Строение и принцип действия:</B></p>
<P>
Помпа  для крови мембранная, работает от сжатого воздуха. Устанавливается на ванну обескровливания. Оттуда через соответствующую систему нержавеющих, кислотостойких труб диаметром 1'' можем транспортировать кровь в произвольное место, в соответствии с технологическим проектом. Помпа  изготовлена из алюминия.
