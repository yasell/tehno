Обвалочная машина предназначена для отделения мяса от костей бедра и голени цыплят-бройлеров.
<BR><IMG src="images/point.gif" border="0" width="1" height="30" alt=""><BR>
<B>Строение и принцип действия:</B>
<P>
Машина изготовлена исключительно из нержавеющих кислотостойких материалов высокого качества и пластика разрешенного для использования в пищевой промышленности. Обвалочная машина является отдельным оборудованием, на котором работает одно лицо. Машина оборудована механизмом выжимания костей голени или бедра с помощью резиновой мембраны. Машина может регулироваться, подстраиваясь под величину голеней и бедер бройлеров.
<BR><IMG src="images/point.gif" border="0" width="1" height="30" alt=""><BR>
<B>Технические данные:</B>
<P>
Мощность электрическая: 0,18kW<br>
Напряжение: 380 - 420 V, 50Hz<br>
Производительность: до ~ 2 400 шт/год - бедра, до ~ 2 000 шт/год - голени<br>
Персонал: 1 лицо
