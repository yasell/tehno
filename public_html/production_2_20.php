<B>Строение и принцип действия:</B>
<P>
Мойка изготовлена из нержавеющих кислотостойких материалов, щетки мойки изготовлены из пластика. Стремена и цепные тележки, которые транспортируются по подвесному конвейеру, проходят через мойку, где происходит их мойка непосредственно душевой подачей воды и вращающими очистительными щетками, расположенными по двум сторонам мойки. Щетки работают от электродвигателей 0,37 кВт через моторедуктор. Щетки регулируются, что позволяет применять мойку для разных видов стремян. Мойка стремян и цепных тележек монтируется к несущей конструкции подвесного конвейера.
