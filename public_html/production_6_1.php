				<h1 class="title  title--red"> Линия воздушного охлаждения мяса птицы </h1>
				Очень важным процессом в технологии забоя птицы является процесс охлаждения. Для достижения длительного срока пригодности тушки самой эффективной системой охлаждения является воздушное или воздушно - капельное охлаждение в тоннеле,  на подвесном цепном конвейере. Конструкция подвесного конвейера может быть разной конфигурации его движения, как горизонтального так и вертикального. Для экономного и рационального  использования производственных площадей, конвейер, как правило,  монтируется в двух уровнях.
