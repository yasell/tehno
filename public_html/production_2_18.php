<BR>Время ошпаривания - 1,5 мин.
<BR>Температура ошпаривания - 65 - 70&#179;C
<BR>Производительность - около 5000 шт./час.
<BR>Энергоноситель - пар или горячая вода (~ 90&#179;C)
