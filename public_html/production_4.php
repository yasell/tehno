<h2>Компактные линии для потрошения любых видов птицы</h2>
Устройство позволяет автоматизировано выполнять потрошения тушки при максимальной загрузке до 800 шт/час. 
Процесс отделения пищевых и непищевых внутренних органов тушки птицы производится путем ряда автоматических последовательных операций на конвейерных линиях потрошения птицы. 
Высокоавтоматизированные польские машины универсальны. Они подходят для любых видов птиц  - куры, утки, гуси, индейки, цесарки, перепела.
<h2>Преимущества оборудования «Szlachet – Stal» для потрошения птицы:</h2>
<ul>
<li>Удобно для большого и малого производств;</li>
<li>Вся линия  устанавливается на отдельно стоящей раме, без потолочных подвесных конструкций;</li>
<li>Легко и недорого обслуживать;</li>
<li>Компактная система, которая состоит из нескольких модулей.</li>
</ul>
