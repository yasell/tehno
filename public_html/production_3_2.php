				<p>Обеспериватель птицы линейный тип SL-2 служит для выдергивания пера разных пород птицы (бройлер, утка, и т.п.). </p>
				<p><b>Строение и принцип действия:</b></p>
				<p>Несущая конструкция и корпус машины изготовлена из нержавеющей, кислотостойкой стали. Обеспериватель пера тип SL - 2 может быть оснащен в 40, 64 или 80 чистящих головок, каждая из которых имеет по 12 резиновых чистящих пальцев (SL-2 имеет 40 головки, маркировка SL-2/40, SL-2 имеет 64 головки, маркировка SL-2/64, 80 головок - SL-2/80).</p>
				<p>Каждый ряд головок приводится в действие с помощью специального ремня отдельным электродвигателем, расположенным в корпусе машины.</p>
                <p>Обеспериватель оснащен механическими ручными подъемниками для регулирования корпусов чистящих головок. В зависимости от величины птицы можно соответственно выставить корпуса. Обеспериватель SL - 2 приспособлен для совместимой работы с конвейерами и стременами производства фирмы "Szlachet - Stal". На случай использования конвейеров других производителей, необходимо связаться с представителями компании "Szlachet - Stal" в целях приспособления машины для выдергивания пера к другому конвейеру.</p>
