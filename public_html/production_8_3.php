<BR><IMG src="images/point.gif" border="0" width="1" height="30" alt=""><BR>
<B>Строение и принцип действия:</B>
<P>
Сепаратор изготовлен из нержавеющей кислотостойкой стали. Основой конструкции сепаратора является корпус в виде ванны, внутри которого находится цилиндрическое сито, установленное на вал. В дальней части корпуса находится  слив воды. Привод сепаратора, который приводит в действие цилиндрическое сито, расположен в верхней части корпуса. Подача отходов или пера происходит через трубное соединение сепаратора. После отведения воды, отходы или перо методом прессования при помощи шнека направляются к исходному отверстию. Длина сепаратора зависит от производительности.
