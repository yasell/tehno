<BR><IMG src="images/point.gif" border="0" width="1" height="30" alt=""><BR>
<B>Строение и принцип действия:</B>
<P>
Несущая конструкция с корпусом изготовлены из нержавеющей стали, кислотостойкой. Привод из двигателя на валы передается с помощью клиновидного ремня, каждый вал приводится в действие отдельным двигателем, спрятанным в нержавеющем корпусе. Обеспериватель оборудован механическим подъемником, с помощью которого можно регулировать валы по высоте. В зависимости от величины птицы и части тушки птицы, которую необходимо общипать можно соответственно отрегулировать валы.
<BR><BR>
<B>Технические данные:</B>
<P>
Используемая мощность: 2 x 1,1 kW
<BR>Напряжение: 380 V
<BR>Количество валов: 2 шт.
<BR>Количество пальцев на валу: 144 
