				<h1 class="title  title--red"> Обеспериватель-мойка Тип MSK – 1 </h1>
				<IMG src="images/production_3_8_2.jpg" border="0" width="228" height="286" align="left" hspace="20" alt="Обеспериватель-мойка Тип MSK – 1">
Обеспериватель-мойка тип MSK-1 служит для совершенной очистки птицы от оставленных перьев и других остатков после обработки птицы в линейных машинах.
<BR><IMG src="images/point.gif" border="0" width="1" height="30" alt=""><BR>
<B>Строение и принцип действия:</B>
<P>
Обеспериватель-мойка изготовлен из нержавеющих, кислотостойких материалов.
<P>
На стабильной конструкционной раме машины симметрично расположены два корпуса, на которых есть валы со специальными резиновыми пальцами L = 265mm.
<P>
Птица на стременах подвесного конвейера двигается между направляющими машины, которые одновременно исполняют роль моющих коллекторов. Вращательные противоположные движения валов с пальцами резиновыми вместе с распыленностью воды из коллекторов обеспечивает надлежащую мойку и устранение остатков пера и других загрязнений с тушек птицы. Конструкция машины позволяет горизонтальное регулирование (схождение/расхождения) и регулирование по высоте чистящих валов и моющих коллекторов, что обеспечивает приспособление оборудования к величине птицы.
