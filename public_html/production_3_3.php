				<h1 class="title  title--red"> Обеспериватель птицы линейный Тип SL-3 </h1>
				<IMG src="images/production_3_3_2.jpg" border="0" width="228" height="286" align="left" hspace="20" alt="Обеспериватель птицы линейный Тип SL-3">
Обеспериватель птицы линейный тип SL-3 служит для выдергивания пера разных пород птицы (бройлер, утка, и т.п.).
<BR><IMG src="images/point.gif" border="0" width="1" height="30" alt=""><BR>
<B>Строение и принцип действия:</B>
<P>
Несущая конструкция и корпус машины изготовлена из нержавеющей, кислотостойкой стали.
<P>
Обеспериватель пера тип SL - 3 может быть оснащен в 60, 72 или 96 чистящих головок, каждая из которых имеет по 12 резиновых чистящих пальцев (SL-3 имеет 60 головок, маркировка SL-3/60, SL-3 имеет 72 головки, маркировка SL-3/72, 96 головок - SL-3/96). Каждый ряд головок приводится в действие с помощью специального ремня отдельным электродвигателем, расположенным в корпусе машины.
<P>
Обеспериватель оснащен механическими ручными подъемниками для регулирования корпусов чистящих головок. В зависимости от величины птицы можно соответственно выставить корпуса. Обеспериватель SL - 3 приспособлен для совместимой работы с конвейерами и стременами производства фирмы "Szlachet - Stal". На случай использования конвейеров других производителей, необходимо связаться с представителями компании "Szlachet - Stal" в целях приспособления машины для выдергивания пера к другому конвейеру.
