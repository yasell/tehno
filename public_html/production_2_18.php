				<h1 class="title  title--red"> Ванна ошпаривания ног Тип OŁ - 1 </h1>
				Шнековое оборудование для ошпаривания ног служит для автоматического ошпаривания отрезанных ног разных пород птицы.
<BR><IMG src="images/point.gif" border="0" width="1" height="30" alt=""><BR>
<B>Строение и принцип действия:</B>
<P>
Шнековое оборудование для ошпаривания ног изготовлено исключительно из нержавеющего и кислотостойкого материала.
<P>
В состав оборудования для ошпаривания входит:
<OL>
<LI>Корыто, усиленное полосами, заканчивается стальными листками
<LI>Шнековый вал, сердечник которого выполнен из трубы, а витки из специально	выгнутой нержавейки со стороны выбросов ног заканчивается симметрично размещенными тремя выбрасывателями
<LI>Повод (передача + двигатель электрический)
<LI>Коллектор, который доставляет в корыто горячую воду
</OL>
Ванна ошпаривания ног оборудована сливным краном и термодатчиком.
Оборудование для ошпаривания должно быть установлено под сбрасывателем ног (это исключает необходимость ручной загрузки). Сброшенные ноги падают прямо в оборудование для ошпаривания. По достижении нужной температуры воды и запуска привода, сброшенные ноги будут перемещаться с помощью витков шнекового вала в направлении выбрасывания и одновременно ошпариваться. Сброшенные на спуск под наклоном ноги направляются непосредственно к машине для очистки ног.
