				<p><b>Строение и принцип действия:</b></p>
				<p>Тоннельная мойка выполнена из профиля и листов кислотостойкой стали в сорте 0H18N9. Это свариваемая конструкция, свободностоящая, самонесущая, которая обеспечивает стабильность и стойкость устройства в процессе работы. Конструкция мойки является модульной, что позволяет легко добавлять модули, напр. модуль сушки.</p>
				<p>Заданием мойки является устранение таких загрязнений как:</p>
				<ul>
				<li>"твердые" загрязнения ( остатки мяса, сыра, жира и тому подобное)</li>
                <li>"жидкие" загрязнения (кровь, молоко, сыворотка, масло, и тому подобное)</li>
				</ul>
				<p>Ящик подается в мойку работником и при помощи цепного транспортера движется сквозь мойку между моющими и ополаскивающими форсунками. После мойки и ополаскивания ящик выходит в зону приема, где забирается работником или попадает на транспортер.</p>
				<p>Подача и оборот воды в мойке обеспечивается насосами высокого давления. Мойка оборудована дозатором моющего средства.</p>
				<p>В зависимости от требований заказчика мойка ящиков может выполнять следующие функции:</p>
				<ul>
				<li>предварительная мойка (смачивание);</li>
                <li>основная мойка;</li>
                <li>ополаскивание;</li>
                <li>обдув.</li>
				</ul>
