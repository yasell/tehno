				<h1 class="title  title--red"> Сито щелевое Tип SŁS - 1/1,5/2/3 </h1>
				Щелевое сито предназначено для очистки сточных вод в разных отраслях производства, в частности в убойных цехах, мясокомбинатах, очистных сооружениях, и тому подобное.
<BR><IMG src="images/point.gif" border="0" width="1" height="30" alt=""><BR>
<B>Строение и принцип действия:</B>
<P>
Устройство имеет накопительный резервуар  с большим объемом, заданием которого является уменьшение турбуленции в стоках. В верхней части резервуара  перелив,  который направляет струю на фильтрующий элемент. Регулируемая диафрагма обеспечивает равномерный проток по всей ширине сита а также предотвращает стремительный проток жидкости. Грязь отделяется от жидкости когда проходит через соответствующий  фильтрующий элемент. Частицы грязи падают самовольно вниз, и отделенная жидкость стекает в главный резервуар  устройства. Величина отделенных частиц  зависит от размера щели и может соответственно представлять: 0,25mm, 0,5mm, 1mm, 1,6mm и другие.  Применяемый фильтрующий элемент,  характеризуется высокой точностью щели,  и очень большой гладкостью рабочей поверхности.  Простая конструкция, а также высокого качества материалы обеспечивают легкое обслуживаниеи беотказную работу устройства.
