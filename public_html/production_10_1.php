				<h1 class="title  title--red"> Ванны нержавеющие </h1>
				Ванна-тележка служит для кратковременного хранения или транспортировки продуктов разных видов.

<p><B>Строение и принцип действия:</B></p>
<P>
Ванна-тележка изготовлена из нержавеющих кислотостойких материалов, потому может использоваться как для продуктов питания так и для отходов. Оборудована парой поворотных колес и парой постоянных колес, что обеспечивает высокую маневренность ванны при перевозке продуктов. Стандартная стационарная ванна выполнена идентично к ванне-тележке, только вместо колес имеет ножки.
