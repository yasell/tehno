				<h1 class="title  title--red"> Сепаратор кишок Тип SJ - 1 </h1>
				Сепаратор служит для отделения воды от кишок после процесса потрошения.

<p><B>Строение и принцип действия:</B></p>
<P>
Сепаратор изготовлен из нержавеющих кислотостойких материалов высокого качества.
<P>
Основные элементы оборудования:
<OL>
<LI>ванна с отводом отсепарированной воды;
<LI>сыто сепарации, установленное на вал;
<LI>повод с цепной передачей.
</OL>
К сепаратору подводится трубопровод транспортировки кишок непосредственно к входному отверстию сепаратора. Кишки перемещаются по спирали в сторону выходного отверстия. Отделенная вода попадает в желоб со спуском в канализационный канал. Отсепарированные кишки после выхода перемещаются пневмотранспортом в склад послезабойных отходов. Привод на сито передается с помощью специальной цепной передачи. 
