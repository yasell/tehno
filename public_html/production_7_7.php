				<h1 class="title  title--red"> Устройство для обвалки Тип WK - 2 </h1>
				<IMG src="images/production_7_7_1.jpg" border="0" width="228" height="250" align="left" hspace="20" alt="Устройство для обвалки Тип WK - 2">
Устройство для обвалки предназначено для отделения мяса от костей бедер и голеней бройлеров.
<BR><IMG src="images/point.gif" border="0" width="1" height="30" alt=""><BR>
<B>Строение и принцип действия:</B>
<P>
Устройство для обвалки монтируется к столу. Изготавливается из нержавеющих кислотостойких материалов и пластика, разрешенного в пищевой промышленности.
<P>
Главные узлы устройства:
<P>
Рама из квадратного профиля
<BR>Поршни
<BR>Направляющие
<BR>Механизм который выпихивает кость
<BR>Резиновая мембрана
<P>
На раме смонтированы два пневматических поршня, которые являют собой привод устройства. Включение устройства происходит с помощью кнопки размещенной в соответствующем месте. Когда кнопка нажата - втулка вместе с поршнем двигается вниз по двум направляющих и таким способом, расположенная между поршнем и резиновой мембраной кость голени или бедра, выжимается через отверстие мембраны. На резиновой мембране остается отделенное мясо. Отделенная кость может быть еще соединена с мясом, тогда необходимо отрезать ее с помощью ножа.
<BR><IMG src="images/point.gif" border="0" width="1" height="30" alt=""><BR>
<B>Технические данные:</B>
<P>
Электрическая мощность: 200 W
<BR>Напряжение: 230 V, 50Hz
<BR>Производительность: ~ 900 шт/час - бедро, ~ 700 шт/час - голень
<BR>Персонал: 1 лицо
