				<h1 class="title  title--red"> Двухножевое устройство для подрезания артерий Тип PG – 2 </h1>

Устройство для подрезания артерий предназначено для автоматического подрезания артерий  бройлеров, подвешенных на стременах подвесного конвейера после процесса электрического глушения.
<BR><IMG src="images/point.gif" border="0" width="1" height="30" alt=""><BR>
<B>Строение и принцип действия:</B>
<P>
Устройство   изготовлено  из нержавеющих кислотостойких материалов. Подрезание происходит оборотными ножами, которые регулируются вручную. Регулирование высоты положения ножа относительно птицы, подвешенной на стременах, проводится через гидравлическую установку. Впереди установки находится специальная нержавеющая направляющая, которая направляет голову птицы к подрезным ножам. Принимая во внимание разные размеры птицы, требуется постоянный контроль за работой оборудования.
